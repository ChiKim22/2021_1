package CH15;

public class Student {

	private Student content;
	private String name, dept;
	private int grade;

	public Student getContent() {
		return content;


	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	String getDept() {
		return dept;
	}
	void setDept(String dept) {
		this.dept = dept;
	}
	int getGrade() {
		return grade;
	}
	void setGrade(int grade) {
		this.grade = grade;
	}


}
